// C# program to illustrate the use of Integer Literals
using System;

class Geeks{

	// Main method
	public static void Main(String[] args)
	{ 
	
		// decimal-form literal
		int a = 101;

		// Hexa-decimal form literal
		int c = 0xFace;
	
		// binary-form literal
		int x = 0b101;
		
		Console.WriteLine(a);
		Console.WriteLine(c);
		Console.WriteLine(x);
	}
}
